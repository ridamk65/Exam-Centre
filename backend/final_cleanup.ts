async function main() {
  console.log('--- PERFORMING FULL DATABASE RESET ---');
  
  // 1. DELETE ALL EXISTING STUDENTS FIRST
  await prisma.student.deleteMany({});
  console.log('Cleaned all existing student records.');

  const lines = ocrData.trim().split('\\n');
  const students = [];

  for (const line of lines) {
    if (line.includes('RegNo StudentName')) continue;
    
    const parts = line.split(' ');
    if (parts.length < 3) continue;

    const reg_no = parts[0];
    const hall = parts[parts.length - 1];
    
    // Middle text: everything between RegNo and Hall
    let middleText = parts.slice(1, parts.length - 1).join(' ').trim();
    
    let finalDept = '';
    let finalName = middleText;

    // Check for exact department matches at the end of the string
    const deptMatchPatterns = [
      { code: 'BCT', match: 'BCT' },
      { code: 'IoT', match: 'IOT' },
      { code: 'IoT', match: 'IoT' },
      { code: 'IoT', match: 'Io' }, 
      { code: 'CS', match: 'CS' },
      { code: 'AI', match: 'AI' }
    ];

    for (const pattern of deptMatchPatterns) {
      if (middleText.toUpperCase().endsWith(pattern.match.toUpperCase())) {
         finalDept = pattern.code;
         finalName = middleText.slice(0, middleText.toUpperCase().lastIndexOf(pattern.match.toUpperCase())).trim();
         break;
      }
    }

    if (!finalDept) {
      finalDept = 'GEN';
      finalName = middleText;
    }

    students.push({
      reg_no,
      name: finalName,
      dept: finalDept,
      year: 3,
      semester: 6,
      phone: '0000000000'
    });
  }

  console.log(`Importing ${students.length} strictly formatted students...`);

  await prisma.student.createMany({
    data: students
  });

  console.log('SUCCESS: Database reset and re-imported with clean departments.');
}

main()
  .catch((e) => {
    console.error(e);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
